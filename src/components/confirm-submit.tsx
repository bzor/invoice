"use client";

/**
 * A submit button that asks for confirmation before letting the form submit.
 * Use inside a <form action={…}> for destructive actions (Void, Delete).
 */
export function ConfirmSubmit({
  message,
  className,
  children,
}: {
  message: string;
  className?: string;
  children: React.ReactNode;
}) {
  return (
    <button
      type="submit"
      className={className}
      onClick={(e) => {
        if (!window.confirm(message)) e.preventDefault();
      }}
    >
      {children}
    </button>
  );
}
